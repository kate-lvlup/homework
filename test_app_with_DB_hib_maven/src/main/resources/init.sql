CREATE TABLE public.ingredients
(
    ingredient_id   bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ingredient_name varchar(255) NOT NULL UNIQUE
);


CREATE TABLE public.recipes
(
    recipe_id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    recipe_name        varchar(255) NOT NULL UNIQUE,
    recipe_description text         NOT NULL
);


CREATE TABLE public.ingredient_recipe
(
    ingredient_recipe_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ingredient_id        bigint NOT NULL,
    foreign key (ingredient_id) references ingredients (ingredient_id),
    recipe_id            bigint NOT NULL,
    foreign key (recipe_id) references recipes (recipe_id)
);

